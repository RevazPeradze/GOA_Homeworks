print("Equal to (==):")
print(5 == 5)   # True
print(5 == 3)   # False

print("\nNot equal to (!=):")
print(5 != 3)  
print(5 != 5)  

print("\nGreater than (>):")
print(5 > 3)   
print(3 > 5)   

print("\nLess than (<):")
print(3 < 5)    
print(5 < 3)    

print("\nGreater than or equal to (>=):")
print(5 >= 3)   
print(5 >= 5)  
print(3 >= 5)   

print("\nLess than or equal to (<=):")
print(3 <= 5)   
print(5 <= 5)   
print(5 <= 3)   

print("\nComparing strings:")
print("apple" == "apple")  
print("apple" == "banana") 
print("apple" != "banana")
print("apple" < "banana")   
print("apple" > "banana")   
